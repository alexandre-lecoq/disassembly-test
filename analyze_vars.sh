#!/bin/bash
# Analyze global variables from assembly files

for addr in 0x2a 0x47c4 0x46eb 0xdbda 0x4 0x2 0x6 0x11c9 0xdbd8 0x23 0xea 0xdbb2 0xce7a 0x2b 0x28e7 0xdbe4 0x47e1 0x479e 0x46d9 0x227d 0xfb 0x4728 0x46df 0x197e 0xdbcd 0x8; do
  echo "===== Analysis for $addr ====="
  grep "\[$addr\]" 01.asm 02.asm 03.asm | head -5
  echo ""
done
